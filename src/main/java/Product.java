/**
 * This is Pojo class for product in electronic shop
 * @author Mohit
 */
public class Product {
    String name;
    String category;
    String price;

    // creating getter and setter for Name
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    // creating getter and setter for category
    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    // creating getter and setter for price
    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
